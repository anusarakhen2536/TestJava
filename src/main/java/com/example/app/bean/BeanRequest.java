package com.example.app.bean;

public class BeanRequest {

	private Integer[] subArray;
	private int summary;

	public BeanRequest() {
	}

	public Integer[] getSubArray() {
		return subArray;
	}

	public void setSubArray(Integer[] subArray) {
		this.subArray = subArray;
	}

	public int getSummary() {
		return summary;
	}

	public void setSummary(int summary) {
		this.summary = summary;
	}

}
